// 实用工具函数：根据 key 和语言获取对应短句
export function getLawOfAttractionPhrase(key: string, lang: string = "zh"): string {
  // 校验语言类型
  const validLangs = ["zh", "en", "tr"];
  if (!validLangs.includes(lang)) {
    console.warn(`Invalid language: ${lang}, defaulting to 'zh'`);
    lang = "zh";
  }

  // 查找对应短句
  const phrase = lawOfAttractionPhrases.find(item => item.key === key);
  return phrase ? phrase[lang as keyof typeof phrase] : `Phrase [${key}] not found`;
}

// 获取随机短语
export function getRandomLawOfAttractionPhrase(lang: string = "zh"): string {
  const randomIndex = Math.floor(Math.random() * lawOfAttractionPhrases.length);
  const phrase = lawOfAttractionPhrases[randomIndex];
  return phrase[lang as keyof typeof phrase];
}